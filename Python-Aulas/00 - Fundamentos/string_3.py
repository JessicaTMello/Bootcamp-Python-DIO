nome = "Jéssica Thaize Mello"

print(nome[0]) #retorna a posição 0
print(nome[-2])
print(nome[:9])  #retorna até a posição da letra 8
print(nome[10:]) #retorna a partir da posição 10 ao final da string
print(nome[10:16]) #retorna um intervalo
print(nome[10:16:2]) #retorna de um intervalo de 2 em 2
print(nome[:]) #retorna do começo ao fim
print(nome[::-1]) #retorna a string invertida

